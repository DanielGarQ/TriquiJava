package Triqui;

public class Juego {
	private String jugadorUno;
	private String jugadorDos;
	private String proximoTurno;
	
	
	public String getJugadorUno() {
		return jugadorUno;
	}
	public void setJugadorUno(String jugadorUno) {
		this.jugadorUno = jugadorUno;
	}
	public String getJugadorDos() {
		return jugadorDos;
	}
	public void setJugadorDos(String jugadorDos) {
		this.jugadorDos = jugadorDos;
	}
	public String getProximoTurno() {
		return proximoTurno;
	}
	public void setProximoTurno(String proximoTurno) {
		this.proximoTurno = proximoTurno;
	}

	

}
